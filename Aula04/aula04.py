# Tipos de int e float
# int -> inteiro
# O tipo int representa qualquer numero inteiro
# Positivo ou negativ. int sem e considerado
# Positivo

print(10)
print(-10)
print(0)

# float -> Numero com ponto flutuante
# O tipo float representa qualquer numero 
# positivo ou negativo com ponto flutuante
# float sem sinal e considerado positivo

print(10.5, 11.5)
print(0.0, -1.5)

# A função type mostra o tipo que o python 
# inferiu ao valor

print(type('Cristian'))
print(type(1))
print(type(1))
print(type(1.1))
